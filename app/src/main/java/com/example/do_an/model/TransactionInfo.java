package com.example.do_an.model;

public class TransactionInfo {
    private String titletran;
    private String pricetran;
    private String date;

    public String getTitletran() {
        return titletran;
    }

    public void setTitletran(String titletran) {
        this.titletran = titletran;
    }

    public String getPricetran() {
        return pricetran;
    }

    public void setPricetran(String pricetran) {
        this.pricetran = pricetran;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
